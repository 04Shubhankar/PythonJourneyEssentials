import math

# Euler's number
print(math.e)

# Pi
print(math.pi)

# Tau
tau = 2 * math.pi
print(tau)

# Infinity
print(math.inf)

# NaN
print(math.nan)

# ceil function
x = 3.14
print(math.ceil(x))  # Output: 4

# floor function
x = 3.99
print(math.floor(x))  # Output: 3

# factorial function
x = 5
print(math.factorial(x))  # Output: 120

# gcd function
a = 24
b = 18
print(math.gcd(a, b))  # Output: 6

# fabs function
x = -3.14
print(math.fabs(x))  # Output: 3.14

# sqrt function
x = 16
print(math.sqrt(x))  # Output: 4.0
